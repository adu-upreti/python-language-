print("#####################CALCULATOR###################")
a = int(input("Enter the first number:"))
b = int(input("Enter the second number:"))
c = str(input("choose operator:"))

if c == "+":
    print("Ans =",a+b)

elif c == "-":
    print("Ans =",a-b)

elif c == "*":
    print("Ans =",a*b)

elif c == "/":
    print("Ans =",a/b)
